function round6(v) { return Math.round(v * 1e6) / 1e6; }

function generateSQL() {
  const lines = [];
  lines.push('-- ============================================');
  lines.push('-- SPK Akreditasi AHP - Seed Alt Comparisons (AHP_FIX_KEL_4)');
  lines.push('-- Generated by generate_alt_seed.js');
  lines.push('-- 21 Sub-Kriteria x 14 Alternatif (upper triangle)');
  lines.push('-- ============================================');
  lines.push('');
  lines.push('DELETE FROM alternative_comparisons;');
  lines.push('');

  for (const [sc, matrix] of Object.entries(matrices)) {
    const subName = sc;
    lines.push(`-- ============================================`);
    lines.push(`-- ${subName}`);
    lines.push(`-- ============================================`);
    lines.push(`INSERT INTO alternative_comparisons (sub_criteria_id, alternative_row_id, alternative_col_id, value) VALUES`);

    const rows = [];
    for (let i = 0; i < 14; i++) {
      for (let j = i + 1; j < 14; j++) {
        const val = round6(matrix[i][j]);
        const rowCode = alts[i];
        const colCode = alts[j];
        rows.push(
          `((SELECT id FROM sub_criteria WHERE code='${subName}'),` +
          `(SELECT id FROM alternatives WHERE code='${rowCode}'),` +
          `(SELECT id FROM alternatives WHERE code='${colCode}'),` +
          `${val})`
        );
      }
    }
    lines.push(rows.join(',\n') + ';');
    lines.push('');
  }

  return lines.join('\n');
}

